let js_builtins =
  {|// --- Builtins ---
let _output_count = 0;
// `print v` outputs pp(v) followed by a newline (the spec: OCaml print_endline;
// native printf "%s\n"; JS VM console.log). The _jsOutput callback contract is
// "one call per print, string WITHOUT the newline — the sink adds framing"
// (same as the VM's output_fn), so only the stdout path appends it here.
// The optional second arg is the compile-time float shape (see _pp). The
// default keeps print.length === 1 so _call's partial application and
// first-class uses (List.iter print xs) behave exactly as before — those
// just get no shape and fall back to heuristic display.
function print(v, _s = null) {
  _output_count++;
  const s = (typeof v === "string") ? v : _pp(v, _s);
  if (typeof globalThis._jsOutput === "function") globalThis._jsOutput(s);
  else if (typeof process !== "undefined") process.stdout.write(s + "\n");
  return undefined;
}
function println(v) {
  _output_count++;
  const s = (typeof v === "string") ? v : _pp(v);
  if (typeof globalThis._jsOutput === "function") globalThis._jsOutput(s + "\n");
  else if (typeof process !== "undefined") process.stdout.write(s + "\n");
  return undefined;
}
function string_of_int(n) { return String(n); }
// OCaml int_of_string semantics: optional sign, 0x/0o/0b prefixes, _ separators.
// All backends must agree (the self-host LEXER parses hex/binary literals with this).
function __parse_int(s0) {
  let s = s0.replace(/_/g, "");
  let sign = 1;
  if (s[0] === "-") { sign = -1; s = s.slice(1); }
  else if (s[0] === "+") { s = s.slice(1); }
  let n;
  if (/^0[xX][0-9a-fA-F]+$/.test(s)) n = parseInt(s.slice(2), 16);
  else if (/^0[oO][0-7]+$/.test(s)) n = parseInt(s.slice(2), 8);
  else if (/^0[bB][01]+$/.test(s)) n = parseInt(s.slice(2), 2);
