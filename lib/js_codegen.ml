(** JS code generation: typed AST -> JavaScript source code.

    Walks a [Typechecker.tprogram] and emits JavaScript as a string. Phase 1:
    direct-style only (no effects/CPS/trampolines). *)

exception Codegen_error of string

let error msg = raise (Codegen_error msg)

(* ---- JS Runtime ---- *)

let js_runtime =
  {|"use strict";
function _call(f, args) {
  while (args.length > 0) {
    const a = f._arity !== undefined ? f._arity : f.length;
    if (a === 0) { f = f(); continue; }
    if (args.length < a) return _partial(f, a, args);
    const taken = args.splice(0, a);
    f = f.apply(null, taken);
  }
  return f;
}
function _partial(fn, arity, args) {
  const p = function() {
    return _call(fn, args.concat(Array.from(arguments)));
  };
  p._arity = arity - args.length;
  return p;
}
function _eq(a, b) {
  if (a === b) return true;
  if (a == null || b == null) return a === b;
  if (typeof a !== "object") return false;
  if (Array.isArray(a)) {
    if (!Array.isArray(b) || a.length !== b.length) return false;
    for (let i = 0; i < a.length; i++) if (!_eq(a[i], b[i])) return false;
    return true;
  }
  if ("_arr" in a && "_arr" in b) {
    if (a._arr.length !== b._arr.length) return false;
    for (let i = 0; i < a._arr.length; i++) if (!_eq(a._arr[i], b._arr[i])) return false;
    return true;
  }
  if ("_hd" in a && "_hd" in b) {
    let ca = a, cb = b;
    while (ca !== null && cb !== null) {
      if (!("_hd" in ca) || !("_hd" in cb)) return ca === cb;
      if (!_eq(ca._hd, cb._hd)) return false;
      ca = ca._tl; cb = cb._tl;
    }
    return ca === cb;
  }
  if ("_tag" in a) return a._tag === b._tag && _eq(a._val, b._val);
  if ("_ref" in a) return _eq(a._ref, b._ref);
  const ka = Object.keys(a), kb = Object.keys(b);
  if (ka.length !== kb.length) return false;
  for (const k of ka) if (!_eq(a[k], b[k])) return false;
  return true;
}
function _compare(a, b) {
  if (a < b) return -1;
  if (a > b) return 1;
  return 0;
}
function _match_fail(loc) { throw new Error("Match failure at " + loc); }
// OCaml/C "%g" with precision 6 — THE float-to-string spec for every backend
// (docs/semantics.md). %e style if the decimal exponent (after rounding to 6
// significant digits) is < -4 or >= 6, else %f style; trailing zeros stripped;
// two-digit exponent. Used bare by string_of_float / show / interpolation.
function __format_g(f) {
  if (Number.isNaN(f)) return "nan";
  if (f === Infinity) return "inf";
  if (f === -Infinity) return "-inf";
  if (f === 0) return Object.is(f, -0) ? "-0" : "0";
  const es = f.toExponential(5);
  const X = parseInt(es.slice(es.indexOf("e") + 1), 10);
  if (X < -4 || X >= 6) {
    let mant = es.slice(0, es.indexOf("e"));
    if (mant.includes(".")) mant = mant.replace(/0+$/, "").replace(/\.$/, "");
    let digits = String(Math.abs(X));
    if (digits.length < 2) digits = "0" + digits;
    return mant + "e" + (X < 0 ? "-" : "+") + digits;
  }
  let s = f.toFixed(Math.max(0, 5 - X));
  if (s.includes(".")) s = s.replace(/0+$/, "").replace(/\.$/, "");
  return s;
}
// Display variant: appends "." when the result could otherwise be read as an
// int ("3."). inf/nan are unambiguous, so no dot. Only DISPLAY appends —
// string_of_float / show / interpolation use bare __format_g.
function __display_float(f) {
  const s = __format_g(f);
  return /[.eni]/.test(s) ? s : s + ".";
}
// _pp(v, _s): display a value. _s is an optional FLOAT SHAPE — a compile-time
// description of where floats live inside v's static type, generated by the
// compiler (BUG-3). MiniML ints and floats are both untagged JS numbers, so a
// whole float (3.0) nested in a structure is indistinguishable from the int 3
// at runtime; the shape is what lets display print it as "3." per the float
// spec. Shapes: "f" (float) | {k:"l"|"a",e:S} (list/array elem) |
// {k:"t",es:[S]} (tuple) | {k:"r",fs:{field:S}} (record) |
// {k:"v",cs:{Ctor:S}} (variant payloads). null/undefined = no float info
// (no floats reachable, unknown type, or first-class use) — heuristics only.
function _pp(v, _s = null) {
  const s = _s;
  if (v === undefined) return "()";
  if (v === null) return "[]";
  if (typeof v === "number") {
    if (!Number.isInteger(v)) return __display_float(v);
    // Whole number: only the static type can say it is a float.
    if (s === "f") return __display_float(v);
    return String(v);
  }
  if (typeof v === "boolean") return String(v);
  if (typeof v === "string") return v;
  if (Array.isArray(v)) {
    const es = (s && s.k === "t") ? s.es : null;
    return "(" + v.map((x, i) => _pp(x, es ? es[i] : null)).join(", ") + ")";
  }
  if (v !== null && typeof v === "object") {
    if ("_arr" in v) {
      const e = (s && s.k === "a") ? s.e : null;
      return "#[" + v._arr.map(x => _pp(x, e)).join("; ") + "]";
    }
    if ("_hd" in v) {
      const e = (s && s.k === "l") ? s.e : null;
      const r = [];
      let c = v;
      while (c !== null && typeof c === "object" && "_hd" in c) { r.push(_pp(c._hd, e)); c = c._tl; }
      return "[" + r.join("; ") + "]";
    }
    if ("_tag" in v) {
      const ps = (s && s.k === "v" && s.cs) ? s.cs[v._name] : null;
      if (v._val !== undefined) {
        const pv = _pp(v._val, ps);
        if (Array.isArray(v._val) || (typeof v._val === "object" && v._val !== null && "_tag" in v._val && v._val._val !== undefined))
          return v._name + " (" + pv + ")";
        return v._name + " " + pv;
      }
      return v._name;
    }
    if ("_ref" in v) return "ref(" + _pp(v._ref, (s && s.k === "ref") ? s.e : null) + ")";
    const fs = (s && s.k === "r") ? s.fs : null;
    return "{ " + Object.entries(v).map(([k,val]) => k + " = " + _pp(val, fs ? fs[k] : null)).join("; ") + " }";
  }
  return String(v);
}
function _throw_break(v) { throw {_tag: "_break", _val: v}; }
function _throw_continue() { throw {_tag: "_continue"}; }
function _throw_fold_cont(v) { throw {_tag: "_fold_cont", _val: v}; }
function _throw_return(v) { throw {_tag: "_return", _val: v}; }
let _h = {};
function _bounce(fn) { fn._tramp = true; return fn; }
const _trampolining = new Set();
function _trampoline(fn, tag) {
  if (tag !== undefined && _trampolining.has(tag)) return fn();
  if (tag !== undefined) _trampolining.add(tag);
  try {
    let result = fn();
    while (typeof result === 'function' && result._tramp) {
      result = result();
    }
    return result;
  } finally { if (tag !== undefined) _trampolining.delete(tag); }
}
function _resolve(v) { while (typeof v === 'function' && v._tramp) v = v(); return v; }
|}

